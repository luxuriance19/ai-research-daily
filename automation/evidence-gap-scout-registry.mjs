const fields = "title,externalIds,publicationDate,authors,url,openAccessPdf,citationCount,referenceCount";

const citationSource = ({ id, label, seedId, topicId, arxivId, paperId, seedIdentityKind, seedScope, primarySeedUrl, semanticScholarPaperId, seedArtifact, reviewPatterns }) => {
  const seedPaperId = paperId || `ARXIV:${arxivId}`;
  return {
    id,
    label,
    seed_id: seedId,
    topic_id: topicId,
    seed_paper_id: seedPaperId,
    seed_identity_kind: seedIdentityKind || "arxiv",
    seed_scope: seedScope,
    review_patterns: reviewPatterns,
    review_pattern_scope: "title-queue-hint-only",
    primary_seed_url: primarySeedUrl || `https://arxiv.org/abs/${arxivId}`,
    ...(seedArtifact ? { seed_artifact: seedArtifact } : {}),
    url: `https://api.semanticscholar.org/graph/v1/paper/${encodeURIComponent(seedPaperId)}/citations?fields=${fields}&limit=100&offset=0`,
    canonical_url: `https://www.semanticscholar.org/paper/${semanticScholarPaperId || encodeURIComponent(seedPaperId)}`,
    role: "citation-discovery",
    authority_tier: "T1",
    independence_group: "semantic-scholar-metadata",
    discovery_only: true,
    exhaustive: false,
    max_bytes: 750_000,
    authentication: "public-no-key-shared-pool",
  };
};

