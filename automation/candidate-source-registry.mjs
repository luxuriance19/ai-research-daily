const githubReleases = ({ id, label, repo, authorityTier = "T4", independenceGroup, lanes }) => ({
  id,
  label,
  url: `https://api.github.com/repos/${repo}/releases?per_page=50&page=1`,
  latest_url: `https://api.github.com/repos/${repo}/releases/latest`,
  canonical_url: `https://github.com/${repo}/releases`,
  format: "github-releases",
  role: "primary-version",
  authority_tier: authorityTier,
  independence_group: independenceGroup,
  lanes,
  max_bytes: 1_000_000,
  minimum_window_hours: 48,
  authentication: "public",
});

const githubFile = ({
  id,
  label,
  repo,
  path,
  independenceGroup,
  lanes,
  expectedTermsAny,
  minimumTextChars = 500,
  jsonFields = [],
  jsonFieldPatterns = {},
  textFieldPatterns = {},
}) => ({
  id,
  label,
  url: `https://api.github.com/repos/${repo}/contents/${path}`,
  canonical_url: `https://github.com/${repo}/blob/main/${path}`,
  format: "github-file",
  role: "primary-version",
  authority_tier: "T4",
  independence_group: independenceGroup,
  lanes,
  expected_path: path,
  expected_terms_any: expectedTermsAny,
  minimum_text_chars: minimumTextChars,
  json_fields: jsonFields,
  json_field_patterns: jsonFieldPatterns,
  text_field_patterns: textFieldPatterns,
  max_bytes: 1_000_000,
  authentication: "public",
});

const githubArtifactTree = ({
  id,
  label,
  repo,
  independenceGroup,
  lanes,
  requiredPathsAll,
  role = "paper-linked-artifact",
  ref = "main",
  versionedPathPattern = "",
  minimumVersionedFiles = 0,
  versionedReviewFlag = "versioned-files-changed-human-canonical-review",
}) => ({
  id,
  label,
  url: `https://api.github.com/repos/${repo}/git/trees/${encodeURIComponent(ref)}?recursive=1`,
  canonical_url: `https://github.com/${repo}/tree/${ref}`,
  format: "github-artifact-tree",
  role,
  authority_tier: "T4",
  independence_group: independenceGroup,
  lanes,
  branch_ref: ref,
  required_paths_all: requiredPathsAll,
  versioned_path_pattern: versionedPathPattern,
  minimum_versioned_files: minimumVersionedFiles,
  versioned_review_flag: versionedReviewFlag,
  max_bytes: 1_000_000,
  authentication: "public",
});

const githubCommits = ({ id, label, repo, path, independenceGroup, lanes, ref = "main" }) => ({
  id,
  label,
  url: path
    ? `https://api.github.com/repos/${repo}/commits?sha=${encodeURIComponent(ref)}&path=${encodeURIComponent(path)}&per_page=50&page=1`
    : `https://api.github.com/repos/${repo}/commits?sha=${encodeURIComponent(ref)}&per_page=50&page=1`,
  canonical_url: path ? `https://github.com/${repo}/commits/${ref}/${path}` : `https://github.com/${repo}/commits/${ref}`,
  format: "github-commits",
  role: "primary-version",
  authority_tier: "T4",
  independence_group: independenceGroup,
  lanes,
  commit_scope: path ? "path" : "repository",
  branch_ref: ref,
  path_scope: path || "",
  minimum_window_hours: 48,
  max_bytes: 1_000_000,
  authentication: "public",
});

